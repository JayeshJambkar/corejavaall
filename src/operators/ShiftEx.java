package operators;

public class ShiftEx {

	public static void main(String[] args) {

		int x = 10 ;  
        // shifting the value by 4 using left shift operator  
        int y = x >> 1 ;  
        // printing the left shifted value  
        System.out.print( " The right shifted value is : \n"+y);
        
        int z = x << 1 ;  
        // printing the left shifted value  
        System.out.print( " The Left shifted value is : "+z);
	
	

	}

}
